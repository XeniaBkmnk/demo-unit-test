package by.demo.itacademy.bakumenko.task2;

public class Calc {
    public int sum(int a, int b) {
        return a + b;
    }

    public int substraction(int a, int b) {
        return a - b;

    }

    public double division(double a, double b) {
        return a / b;

    }

    public int multiplication(int a, int b) {
        return a * b;

    }
}
